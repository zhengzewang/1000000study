public class EqualsDemo {
	
	public static void main(String[] args) {
		
		Float float1 = new Float("20.3f");
		Float float2 = 20.3f;
		float f = 20.3f;
		System.out.println(float1 == float2);
		System.out.println(float1 == f);
		System.out.println(float2 == f);
		
	}

}
